#include <mp.h>

static void _dummy_printk(void *user, char *fmt, ...) { }

mp_printk_call_t mp_printk_call;
void *mp_printk_user;

mp_ret_t mp_printk_set(mp_printk_call_t call, void *user) {
	mp_printk_call = call;
	mp_printk_user = user;
	return(TRUE);
}

mp_ret_t mp_printk_unset() {
	mp_printk_call = _dummy_printk;
	return(TRUE);
}
